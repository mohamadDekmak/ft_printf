#include "libft.h"

void	*ft_calloc(size_t nmemb, size_t size)
{
	void			*tmp;

	if (size != 0 && nmemb > (__SIZE_MAX__ / size))
		return (NULL);
	tmp = malloc(nmemb * size);
	if (!tmp)
		return (NULL);
	ft_memset(tmp, 0, nmemb * size);
	return (tmp);
}
